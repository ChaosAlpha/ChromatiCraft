	@Override
	public void generate(ChunkSplicedGenerationCache world, int x, int y, int z) {
		this.doGenerate(world, x, y, z);
		this.doGenerate2(world, x, y, z);
		this.doGenerate3(world, x, y, z);

		for (int i = -1; i <= 25; i++) {
			for (int k = 0; k <= 8; k++) {
				world.setBlock(x+i, y+k, z-1, b, ms);
				world.setBlock(x+i, y+k, z+25, b, ms);
				world.setBlock(x-1, y+k, z+i, b, ms);
				world.setBlock(x+25, y+k, z+i, b, ms);
			}
		}

		for (int i = 0; i <= 24; i++) {
			for (int k = 3; k <= 8; k++) {
				int m = k == 3 && i%2 == 0 ? ml : ms;
				world.setBlock(x+i, y+k, z+0, b, m);
				world.setBlock(x+i, y+k, z+24, b, m);
				world.setBlock(x+0, y+k, z+i, b, m);
				world.setBlock(x+24, y+k, z+i, b, m);
			}
		}

		for (int i = 1; i < 24; i++) {
			for (int k = 1; k < 24; k++) {
				int dx = x+i;
				int dz = z+k;
				world.setBlock(dx, y+3, dz, ChromaBlocks.SPECIALSHIELD.getBlockInstance(), mg);

				//ReikaJavaLibrary.pConsole(((8+dx%8)%8)+":"+((8+dz%8)%8)+" > "+lightPattern[(8+dx%8)%8][(8+dz%8)%8]);

				world.setBlock(dx, y+0, dz, b, /*rand.nextInt(4) == 0*/lightPattern[(8+(dx+4)%8)%8][(8+(dz+4)%8)%8] ? ml : ms);
				world.setBlock(dx, y+6, dz, Blocks.air);
				world.setBlock(dx, y+7, dz, b, /*rand.nextInt(4) == 0*/lightPattern[(8+dx%8)%8][(8+dz%8)%8] ? ml : ms);
				world.setBlock(dx, y+8, dz, b, ms);
			}
		}

		world.setBlock(x+25, y+5, z+11, b, ml);

		world.setBlock(x+25, y+1, z+11, Blocks.air);
		world.setBlock(x+25, y+2, z+11, Blocks.air);

		world.setBlock(x+25, y+4, z+14, Blocks.air);
		world.setBlock(x+25, y+5, z+14, Blocks.air);
		world.setBlock(x+25, y+4, z+8, Blocks.air);
		world.setBlock(x+25, y+5, z+8, Blocks.air);

		world.setBlock(x+24, y+4, z+14, Blocks.air);
		world.setBlock(x+24, y+5, z+14, Blocks.air);
		world.setBlock(x+24, y+4, z+8, Blocks.air);
		world.setBlock(x+24, y+5, z+8, Blocks.air);
	}

